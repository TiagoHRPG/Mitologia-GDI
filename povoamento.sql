-------------------
-- CRIACAO DO BD --
-------------------

-- CRIA TABELA POVO
CREATE TABLE POVO(
    Nac_epoca NUMBER,
    Nac_regiao VARCHAR(50),
    CONSTRAINT PK_POVO PRIMARY KEY (Nac_epoca, Nac_regiao)
);

-- CRIA TABELA MITOLOGIA
CREATE TABLE MITOLOGIA(
    Nac_epoca NUMBER,
    Nac_regiao VARCHAR(50),
    Eh_politeista VARCHAR(1) DEFAULT 'N',
    CONSTRAINT PK_MITOLOGIA PRIMARY KEY (Nac_epoca, Nac_regiao),
    CONSTRAINT FK_POVO_MIT FOREIGN KEY (Nac_epoca,Nac_regiao) REFERENCES POVO (Nac_epoca, Nac_regiao)
);

-- CRIA TABELA DEUS
CREATE TABLE DEUS(
    Nome_deus VARCHAR(50),
    Funcao_1 VARCHAR(50) DEFAULT 'Nao definido',
    Funcao_2 VARCHAR(50) DEFAULT 'Nao definido',
    CONSTRAINT PK_DEUS PRIMARY KEY (Nome_deus)
);

-- CRIA TABELA TERRITORIO
CREATE TABLE TERRITORIO(
    Nome_territorio VARCHAR(50),
    Nome_deus VARCHAR(50) UNIQUE,
    CONSTRAINT PK_TERRITORIO PRIMARY KEY(Nome_territorio),
    CONSTRAINT FK_TERRITORIO_DEUS FOREIGN KEY (Nome_deus) REFERENCES DEUS(Nome_deus)
);

-- CRIA TABELA CRIATURA
CREATE TABLE CRIATURA(
    Nome_criatura VARCHAR(50),
    Tipo_habilidade VARCHAR(50) DEFAULT 'Nao definido', 
    Nac_regiao VARCHAR(50) NOT NULL,
    Nac_epoca NUMBER NOT NULL,
    CONSTRAINT PK_CRIATURA PRIMARY KEY (Nome_criatura),
    CONSTRAINT FK_NAC FOREIGN KEY (Nac_regiao, Nac_epoca) REFERENCES MITOLOGIA(Nac_regiao, Nac_epoca)
    
);

-- CRIA TABELA REGIAO
CREATE TABLE REGIAO(
    Nome_regiao VARCHAR(50),
    CONSTRAINT PK_REGIAO PRIMARY KEY (Nome_regiao)
);

-- CRIA TABELA HEROI
CREATE TABLE HEROI(
    Nome_heroi VARCHAR(50),
    Nac_epoca NUMBER NOT NULL,
    Nac_regiao VARCHAR(50) NOT NULL,
    Mortes NUMBER NOT NULL,
    CONSTRAINT PK_HEROI PRIMARY KEY (Nome_heroi),
    CONSTRAINT FK_HEROI_POVO FOREIGN KEY (Nac_epoca, Nac_regiao) REFERENCES POVO (Nac_epoca, Nac_regiao),
    CONSTRAINT CK_MORTES CHECK (Mortes >= 0)
);

-- CRIA TABELA SEMIDEUS
CREATE TABLE SEMIDEUS(
    Nome_semideus VARCHAR(50),
    Poder VARCHAR(50) DEFAULT 'Nenhum',
    Nome_deus VARCHAR(50) NOT NULL,
    CONSTRAINT PK_SEMIDEUS PRIMARY KEY (Nome_semideus),
    CONSTRAINT FK_SEMIDEUS_HEROI FOREIGN KEY (Nome_semideus) REFERENCES HEROI(Nome_heroi),
    CONSTRAINT FK_SEMIDEUS_DEUS FOREIGN KEY (Nome_deus) REFERENCES DEUS(Nome_deus)
);

-- CRIA TABELA HUMANO
CREATE TABLE HUMANO(
    Nome_humano VARCHAR(50),
    CONSTRAINT PK_HUMANO PRIMARY KEY (Nome_humano),
    CONSTRAINT FK_HUMANO_HEROI FOREIGN KEY (Nome_humano) REFERENCES HEROI(Nome_heroi)
);

-- CRIA TABELA FEITOS
CREATE TABLE FEITOS(
    Nome_heroi VARCHAR(50),
    Num_feito NUMBER,
    Descricao_feito VARCHAR(140) DEFAULT 'Desconhecido',
    CONSTRAINT PK_FEITOS PRIMARY KEY (Nome_heroi, Num_feito),
    CONSTRAINT FK_FEITOS_HEROI FOREIGN KEY (Nome_heroi) REFERENCES HEROI(Nome_heroi)
);

-- CRIA TABELA ADORA
CREATE TABLE ADORA(
    Nome_deus VARCHAR(50),
    Nac_epoca NUMBER NOT NULL,
    Nac_regiao VARCHAR(50) NOT NULL,
    CONSTRAINT PK_ADORA PRIMARY KEY (Nome_deus),
    CONSTRAINT FK_ADORA FOREIGN KEY (Nome_deus) REFERENCES DEUS(Nome_deus),
    CONSTRAINT FK_ADORA_MIT FOREIGN KEY (Nac_epoca, Nac_regiao) REFERENCES MITOLOGIA(Nac_epoca, Nac_regiao)
);

-- CRIA TABELA TEMPLO
CREATE TABLE TEMPLO(
    Nome_templo VARCHAR(50),
    Nome_deus VARCHAR(50) NOT NULL,
    CONSTRAINT PK_TEMPLO PRIMARY KEY (Nome_templo),
    CONSTRAINT FK_TEMPLO FOREIGN KEY (Nome_deus) REFERENCES ADORA(Nome_deus)
);

-- CRIA TABELA ANCESTRALIDADE
CREATE TABLE ANCESTRALIDADE(
    Nome_ancestral VARCHAR(50),
    Nome_descendente VARCHAR(50),
    Parentesco VARCHAR(50) NOT NULL,
    CONSTRAINT PK_ANCESTRAL PRIMARY KEY (Nome_ancestral, Nome_descendente),
    CONSTRAINT FK_ANCESTRAL_ANCESTRAL FOREIGN KEY (Nome_ancestral) REFERENCES DEUS(Nome_deus),
    CONSTRAINT FK_ANCESTRAL_DESCENDENTE FOREIGN KEY (Nome_descendente) REFERENCES DEUS(Nome_deus)  
);

-- CRIA TABELA ENFRENTA
CREATE TABLE ENFRENTA(
    Nome_heroi VARCHAR(50),
    Nome_regiao VARCHAR(50),
    Nome_criatura VARCHAR(50),
    Data_luta DATE,
    CONSTRAINT PK_ENFRENTA PRIMARY KEY (Nome_heroi, Nome_regiao, Nome_criatura, Data_luta),
    CONSTRAINT FK_ENFRENTA_HEROI FOREIGN KEY (Nome_heroi) REFERENCES HEROI(Nome_heroi),
    CONSTRAINT FK_ENFRENTA_REGIAO FOREIGN KEY (Nome_regiao) REFERENCES REGIAO(Nome_regiao),
    CONSTRAINT FK_ENFRENTA_CRIATURA FOREIGN KEY (Nome_criatura) REFERENCES CRIATURA(Nome_criatura)
);

